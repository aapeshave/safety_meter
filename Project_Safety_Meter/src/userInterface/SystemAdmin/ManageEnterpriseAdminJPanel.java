    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        
        for(String networkKeys : system.getNetworkList().keySet()){
            Network currentNetwork = system.getNetworkList().get(networkKeys);
            for(Integer enterpriseKeys : currentNetwork.getEnterpriseDirectory().getEnterpriseList().keySet()){
                Enterprise currentEnterprise = currentNetwork.getEnterpriseDirectory().getEnterpriseList().get(enterpriseKeys);
                for(String userKeys : currentEnterprise.getUserAccountDirectory().getUserAccountDirectory().keySet()){
                    UserAccount currentUser = currentEnterprise.getUserAccountDirectory().getUserAccountDirectory().get(userKeys);
                    Object[] row = new Object[3];
                    row[0] = currentEnterprise.getName();
                    row[1] = currentNetwork.getName();
                    row[2] = currentUser.getUsername();

                    model.addRow(row);
                }
            }
        }
    }
    
    
    private void populateNetworkComboBox(){
        networkJComboBox.removeAllItems();
        
        for(String networkKeys : system.getNetworkList().keySet()){
            Network network = system.getNetworkList().get(networkKeys);
            networkJComboBox.addItem(network);
        }
    }
    
    private void populateEnterpriseComboBox(Network network){
        enterpriseJComboBox.removeAllItems();
        
        for (Integer enterpriseKeys : network.getEnterpriseDirectory().getEnterpriseList().keySet()){
            enterpriseJComboBox.addItem(network.getEnterpriseDirectory().getEnterpriseList().get(enterpriseKeys));
        }
        
    }
