    private void addAccountJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAccountJButtonActionPerformed
        
        int selectedRow = userAccountJTable.getSelectedRow();
        if(selectedRow>=0){
            this.person = (Person) userAccountJTable.getValueAt(selectedRow, 0);
            
            if(person.getUserAccount().getUsername() == null){
                userNameTextField.setText(this.person.getSSN());
                showFields(true);
            }
            else{
                JOptionPane.showMessageDialog(passwordField, "UserAccount Already Exists");
            }
            
        }
        else{
            JOptionPane.showMessageDialog(passwordField, "Please Select a Row");
        }
    }//GEN-LAST:event_addAccountJButtonActionPerformed

    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        String userName = userNameTextField.getText();
        String password = String.valueOf(passwordField.getPassword()).trim();
        
        UserAccount userAccount  = person.getUserAccount();
        if(userAccount == null){
            userAccount = new UserAccount();
        }
        userAccount.setUsername(userName);
        userAccount.setPassword(password);
        userAccount.setRole(new PersonRole());
        
        JOptionPane.showMessageDialog(passwordField, "UserAccount is created");
        populateTable();
    }//GEN-LAST:event_addJButtonActionPerformed


