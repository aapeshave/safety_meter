    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) personReportJTable.getModel();
        model.setRowCount(0);
        
        for(String personKeys : incident.getInvolvedpeople().getPersonList().keySet()){
            Person person = incident.getInvolvedpeople().getPersonList().get(personKeys);
            Object row[] = new Object[3];
            row[0] = person;
            
            row[1] = person.getAddress();
            row[2] = person.getDoesHaveLicense();
            model.addRow(row);
        }
    }
