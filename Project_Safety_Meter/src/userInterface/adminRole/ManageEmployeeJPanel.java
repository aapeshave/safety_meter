    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        String firstName = fnameJTextField.getText().trim();
        String lastName = lnameJTextField.getText().trim();
        String SSN = ssnJTextField.getText();
        String email = emailJTextField.getText();
        String in = incomeTextField.getText().trim();
        int income =0;
        String workPhone = wPhoneJTextField.getText().trim();
        DateFormat df = new SimpleDateFormat("MM/DD/YYYY");
        Date birthdate = new Date();
        String address = addressTextArea.getText().trim();
        
       
        
        if(validateName(firstName) && validateName(lastName)){
            if(validateSSN(SSN)){
                if(validateEmail(email)){
                    if(validatePhone(workPhone)){
                        try{
                                income = Integer.parseInt(in);
                                birthdate = new Date(birthdateTextField.getText());
                        }
                        catch(Exception e){
                            JOptionPane.showMessageDialog(birthdateTextField, "Please Enter Valid Date (MM/DD/YYYY)");
                            birthdateTextField.setText("");
                        }
                        if(!address.isEmpty()){
                            Person newPerson = new Person();
                            Employee newEmployee = new Employee();
                        
                            newPerson.setEmail(email);
                            newPerson.setFirstName(firstName);
                            newPerson.setLastName(lastName);
                            newPerson.setSSN(SSN);
                            newPerson.setBirthdate(birthdate);
                            newPerson.setAddress(address);
                            newPerson.setMonthlyIncome(income);
                            
                            newEmployee.setMonthlyIncome(income);
                            newEmployee.setPerson(newPerson);
                            newEmployee.setWorkPhone(workPhone);
                            organization.getEmployeeDirectory().addEmployee(newEmployee);
                            netoworkPersonList.addPerson(newPerson);    //Add person for future records
                            
                            JOptionPane.showMessageDialog(addJButton, "Records for "+ newPerson.getFirstName() + " are added!");
                            populateTable(organization);
                            resetFields();
                        }
                        else{
                            JOptionPane.showMessageDialog(addJButton, "Please Enter Valid Address");
                            addressTextArea.setText("");
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(addJButton, "Please Enter 10 digit Phone Number");
                        wPhoneJTextField.setText("");
                    }
                }
                else{
                    JOptionPane.showMessageDialog(addJButton, "Please Enter Valid Email");
                    emailJTextField.setText("");
                }
            }
            else{
               JOptionPane.showMessageDialog(addJButton, "Please Enter Valid SSN"); 
               ssnJTextField.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(addJButton, "Please Enter Valid Names");
            fnameJTextField.setText("");
            lnameJTextField.setText("");
        }
    }//GEN-LAST:event_addJButtonActionPerformed

