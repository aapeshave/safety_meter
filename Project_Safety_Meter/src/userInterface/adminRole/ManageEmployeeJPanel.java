    public void populateOrganizationComboBox(){
        organizationJComboBox.removeAllItems();
        
        for(Integer organizationKeys : organizationDir.getOrganizationDirectory().keySet()){
            Organization organization = organizationDir.getOrganizationDirectory().get(organizationKeys);
            organizationJComboBox.addItem(organization);
        }
       
    }
    
    

    private void populateTable(Organization organization){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        for(String employeeKeys : organization.getEmployeeDirectory().getEmployeeDirectory().keySet()){
            Employee employee = organization.getEmployeeDirectory().getEmployeeDirectory().get(employeeKeys);
            Object[] row = new Object[4];
            row[0] = employee.getPerson().getSSN();
            row[1] = employee.getPerson().getFirstName();
            row[2] = employee.getPerson().getLastName();
            row[3] = employee.getWorkPhone();
            model.addRow(row);
        }
    }
