    private void sendRequstJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendRequstJButtonActionPerformed
        int i = personReportJTable.getSelectedRow();
        if(i<0){
            JOptionPane.showMessageDialog(null, "Please Select Any Person");
        }
        else{
            Person selectedPerson = (Person) personReportJTable.getValueAt(i, 0);
            if(selectedPerson.getDoesHaveLicense()==1){
                //CrimeIncident crime = (CrimeIncident) personReportJTable.getValueAt(i, 4);
            
                RevokeLicenseRequestJPanel revoke = new RevokeLicenseRequestJPanel(userProcessContainer, personDirectory, selectedPerson, user, enterprise,network);
                userProcessContainer.add(revoke);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            else{
                JOptionPane.showMessageDialog(backJButton, "Selected Person Doesn't Have Any License");
            }
            
        }
    }//GEN-LAST:event_sendRequstJButtonActionPerformed


