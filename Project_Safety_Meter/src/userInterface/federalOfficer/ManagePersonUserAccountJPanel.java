    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) userAccountJTable.getModel();
        model.setRowCount(0);
        
        for(String userKeys : personDirectory.getPersonList().keySet()){
            Person person = personDirectory.getPersonList().get(userKeys);
            Object[] row = new Object[4];
            row[0] = person;
            row[1] = userKeys;
            if(person.getUserAccount().getUsername()==null){
                row[2] = "User-Account Not Created";
            }
            else{
                row[2] = person.getUserAccount().getUsername();
                row[3] = person.getUserAccount().getPassword();
            }
            
            model.addRow(row);
        }
    }

    private void showFields(boolean b) {
        jLabel2.setVisible(b);
        jLabel3.setVisible(b);
        userNameTextField.setVisible(b);
        passwordField.setVisible(b);
        addJButton.setVisible(b);
    }
