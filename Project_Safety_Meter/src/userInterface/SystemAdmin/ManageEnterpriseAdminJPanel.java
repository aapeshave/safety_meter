    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed

        Enterprise enterprise = (Enterprise) enterpriseJComboBox.getSelectedItem();

        String username = usernameJTextField.getText().trim();
        String password = String.valueOf(passwordJPasswordField.getPassword()).trim();
        String name = nameJTextField.getText().trim();

        if(validateName(name)){
            if(validateSSN(ssnJTextField.getText())){
                Employee newEmployee = new Employee();
                Person newPerson = new Person();
                newPerson.setSSN(ssnJTextField.getText());
                newPerson.setFirstName(name);
                newEmployee.setEmployerName(enterprise.getName());
                newEmployee.setPerson(newPerson);
        
                enterprise.getEmployeeDirectory().addEmployee(newEmployee);
                if(enterprise.getUserAccountDirectory().checkUniqueUsername(username)){
                    enterprise.getUserAccountDirectory().addUserAccount(username, password, newEmployee, new AdminRole());
                    populateTable();
                    JOptionPane.showMessageDialog(submitJButton, "Enterprise Administator Added Successfully!");
                    resetFields();
                }
                else{
                    JOptionPane.showMessageDialog(submitJButton, "Username Already Exists");
                    usernameJTextField.setText("");
                }
            }
            else{
                JOptionPane.showMessageDialog(backJButton, "Enter a Valid SSN");
                ssnJTextField.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(backJButton, "Enter a Valid Name");
            nameJTextField.setText("");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

